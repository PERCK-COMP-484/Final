                  <div class="terms">
                    <dl>
                      <dt>Linear Equation in One Variable</dt>
                      <dd>
                        An equation denoted as \(A + B = C\) where A, B, and C
                        \(\in\) \(\mathbb R_{\ne 0}\).
                      </dd>
                      <dt>Addition Property of Equality</dt>
                      <dd>
                        Given \(a = b\), then by the Addition Property of Equality,
                        \(a + c = b + c\) where a, b and, c \(\in\)
                        \(\mathbb{R}\).
                      </dd>
                      <dt>Multiplication Property of Equality</dt>
                      <dd>
                        Given \(a = b\), then by the Multiplcation Property of
                        Equality, \(ac = bc\) or \(a \over c\) = \(b \over c\) where
                        a and b \(\in\) \(\mathbb{R}\), and c \(\in\) \(\mathbb R_{\ne 0}\).
                      </dd>
                      <dt>Distributive Property</dt>
                      <dd>
                        Given an expression in the form of a(b + c), then by the
                        Distributive Property, \(a(b + c) = ab + ac\) where a, b, and
                        c \(\in\) \(\mathbb{R}\).
                      </dd>
                    </dl>
