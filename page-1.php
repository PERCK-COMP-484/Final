                <div id="linear-equation-block">
                  <h2>
                    How to Solve a Simple Linear Equation
                  </h2>
                  <div id="linear-equation-steps">
                    <ol>
                      <li>
                        The goal is to isolate &ldquo;x&rdquo; or some variable onto
                        one side of the equation.
                      </li>
                      <li>
                        When working backwards to find &ldquo;x&rdquo; or some
                        variable, we would need to use the opposite operator to cancel
                        numbers from both sides of the equation. The opposite of
                        addition is substraction and the opposite of multiplication is
                        division.
                      </li>
                      <li>
                        The order of operations or PEMDAS, for short, are a set of
                        rules in which certain math procedures need to be performed
                        first. From the highest priority, it is parenthesis,
                        exponents, multiplication or division, and addition or
                        substraction. When working backwards to find &ldquo;x&rdquo;
                        or some variable, you will also need to flip the way you
                        normally would do your order of operations.
                      </li>
                      <li>
                        Once you work your way and isolated &ldquo;x&rdquo; or some
                        variable, then you have finished the problem.
                      </li>
                      <li>
                        To double check your answer, plug the value back into the
                        variable and the two sides (Left-Hand Side and Right-Hand
                        Side) should be equal to one another.
                      </li>
                    </ol>
                  </div>
                  <div class="example-block">
                    <h2>
                      Examples
                    </h2>
                    <div class="example-one">
                      <h3>
                        Example One
                      </h3>
                      <p>
                        Solve for x.
                      </p>
                      <p>
                        \(8x + 8 = 48\) \(\qquad\)subtract 8 from both sides<br />
                        \(\qquad\)-8 \(\quad\) -8 <br />
                        \(8x = 40\) \(\qquad\) divide 8 from both sides<br />
                        \({8x \over 8} = {40 \over 8}\)<br />
                        \(x = 5\) \(\qquad\) solved
                      </p>
                      <p>
                        \(8x + 8 = 48\) is a fairly simple algebraic linear equation
                        you will ever come across. As you can see here to solve our
                        \(x\) we will need to isolate it. In the directions stated
                        above, we would need to do a reverse PEMDAS. First to get rid
                        of the &ldquo;+ 8&rdquo;, we can remove it by subtracting 8
                        from both sides since subtraction is the opposite of addition.
                        Once we subtract 8 from both sides, notice how we have another
                        8 multiplying to our \(x\). Since we need to solve by
                        isolating x, we would need to divide 8 from both sides since
                        division is the opposite of multiplication. Once you have done
                        so, you are left with your answer.
                      </p>
                      <div class="example-two">
                        <h3>
                          Example Two
                        </h3>
                        <p>
                          Solve for y.
                        </p>
                        <p>
                          \({7 \over 15}y = {2 \over 5}y - {1 \over 3} \) \(\qquad\)
                          multiply both sides by 15<br />
                          \(15 * {7 \over 15}y = 15({2 \over 5}y - {1 \over 3}) \)
                          \(\qquad\) distributive property<br />
                          \(15 * {7 \over 15}y = 15 * {2 \over 5}y - 15 * {1 \over 3}
                          \)<br />
                          \(7 = 6y - 5\) \(\qquad\) add 5 to both sides<br />
                          +5\(\qquad\) +5<br />
                          \(12 = 6y\) \(\qquad\) divide 6 from both sides <br />
                          \(y = 2\) \(\qquad\) solved
                        </p>
                        <p>
                          As you can see here, we are dealing with harder problems
                          now. Don't be alarm, fractions are not as bad as one would
                          think. Because we're working backwards to find \(y\), we are
                          going to use the reversed PEMDAS to solve for it. Whenever
                          you are dealing with fractions such as the one above, it is
                          best to get rid of the denominator. Since we know that
                          multiplication is the opposite of division, we can multiple
                          both the left-hand and right-hand side by the LCD or least
                          common denominator. When we multiply both sides of the
                          fraction by the LCD a.k.a 15, no matter what happens, we
                          would always get a whole number as you can see on the
                          \(4^{th}\) line. Since we got rid of the denominator and we
                          are left with whole numbers, the equation is much easier to
                          solve. From there we can easily add 5 to both sides, then
                          divide both sides by 6; thus; solving for y.
                        </p>
